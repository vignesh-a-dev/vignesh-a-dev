public class task2_oddno_triangle {
    public static void main(String[] args) { 
        for (int i = 5; i > 0; i--) {
            for (int j = 0; j < i; j++) {
                System.out.print("* "); // print *
            }
            System.out.println(); // move to the new line
        }
        
    }
}
